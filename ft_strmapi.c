#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	nb;
	size_t	i;
	char	*p;

	i = 0;
	if (!s)
		return (NULL);
	nb = ft_strlen(s);
	p = malloc(sizeof(char) * nb + 1);
	if (!p)
		return (NULL);
	while (s[i])
	{
		p[i] = f(i, s[i]);
		i++;
	}
	p[i] = '\0';
	return (p);
}
