#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c <= '9' && c >= '0')
		|| ((c <= 'z' && c >= 'a')
			|| (c >= 'A' && c <= 'Z')))
	{
		return (1);
	}
	else
		return (0);
}
